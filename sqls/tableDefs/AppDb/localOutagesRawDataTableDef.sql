CREATE TABLE OUTAGE_EVENTS (
  id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
  PWC_ID NUMBER,
  ELEMENT_ID NUMBER(*, 0) NOT NULL,
  ELEMENT_NAME VARCHAR2(100) NOT NULL,
  OWNERS VARCHAR2(1000),
  ENTITY_ID NUMBER(*, 0) NOT NULL,
  ENTITY_NAME VARCHAR2(100) NOT NULL,
  CAPACITY VARCHAR2(100),
  OUTAGE_DATETIME TIMESTAMP(6) NOT NULL,
  REVIVED_DATETIME TIMESTAMP(6),
  CREATED_DATETIME TIMESTAMP(6),
  MODIFIED_DATETIME TIMESTAMP(6),
  SHUTDOWN_TAG VARCHAR2(100),
  SHUTDOWN_TAG_ID NUMBER(38, 0),
  SHUTDOWN_TYPENAME VARCHAR2(100),
  SHUT_DOWN_TYPE_ID NUMBER(*, 0),
  OUTAGE_REMARKS VARCHAR2(500),
  REASON_ID NUMBER,
  REASON VARCHAR2(1000),
  REVIVAL_REMARKS VARCHAR2(500),
  REGION_ID NUMBER,
  SHUTDOWNREQUEST_ID NUMBER
);